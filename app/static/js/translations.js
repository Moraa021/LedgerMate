/**
 * Sends language choice to Flask and reloads
 */
function changeLanguage(lang) {
    console.log('Switching to:', lang);
    fetch('/set-language', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ language: lang })
    })
    .then(response => response.json())
    .then(data => {
        if (data.success) {
            location.reload();
        }
    })
    .catch(error => {
        console.error('Language Error:', error);
        location.reload(); // Reload anyway to try and sync
    });
}

/**
 * Scans DOM for data-en/sw attributes and updates content
 */
function updateUIText(lang) {
    const elements = document.querySelectorAll('[data-en], [data-sw], [data-en-placeholder], [data-sw-placeholder]');
    
    elements.forEach(el => {
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
            const placeholder = el.getAttribute(`data-${lang}-placeholder`);
            if (placeholder) el.placeholder = placeholder;
        } else {
            const translation = el.getAttribute(`data-${lang}`);
            if (translation) el.textContent = translation;
        }
    });
}

/**
 * Guide Modal logic
 */
function showQuickGuide() {
    const currentLang = document.documentElement.lang || 'en';
    const guide = translations[currentLang];
    
    const modal = document.createElement('div');
    modal.className = 'guide-modal';
    // Style this in your main.css, or use this inline fallback:
    modal.style.cssText = "position:fixed; top:50%; left:50%; transform:translate(-50%, -50%); background:white; padding:20px; z-index:9999; border-radius:12px; box-shadow:0 10px 40px rgba(0,0,0,0.3); width:90%; max-width:400px;";
    
    modal.innerHTML = `
        <div class="guide-content">
            <h3 style="margin-top:0">${guide.quick_guide}</h3>
            <ol style="padding-left:20px; line-height:1.5;">
                <li>${guide.guide_step1}</li>
                <li>${guide.guide_step2}</li>
                <li>${guide.guide_step3}</li>
                <li>${guide.guide_step4}</li>
            </ol>
            <div style="text-align:right">
                <button class="btn btn-primary" onclick="this.closest('.guide-modal').remove()">${guide.got_it}</button>
            </div>
        </div>
    `;
    document.body.appendChild(modal);
}

// Global Initialization
document.addEventListener('DOMContentLoaded', () => {
    const currentLang = document.documentElement.lang || 'en';
    updateUIText(currentLang);

    if (!localStorage.getItem('guideShown')) {
        showQuickGuide();
        localStorage.setItem('guideShown', 'true');
    }
});